package com.syntex.class7;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		int num=90;
		num=num+1;
		System.out.println(num);
		
		num++;
		System.out.println(num);
		
		num--;
		System.out.println(num);
		
		int i=10;
		i--;
		i--;
		System.out.println(i); 
		
		

	}

}
